/**
 * Generate TypeScript token modules from the Tokens Studio exports.
 *
 * The platform has no TypeScript export format, so this script combines
 * the two exports the release workflow already pulls:
 *
 *  - the DTCG export (`src/tokens/dtcg/`) provides the token tree
 *    structure and `$type` metadata, but keeps `{alias}` references and
 *    unevaluated color formulas
 *  - the CSS export (`src/tokens/css/`) provides evaluated concrete
 *    values (the platform's formula engine only runs for CSS), joined
 *    across files by `var(--eds-*)` chains
 *
 * For every DTCG leaf the script derives the CSS custom property name
 * (`eds-` + path joined with `-`), dereferences the `var()` chain in the
 * relevant dimension context, and converts values to React Native
 * friendly forms: oklch()/rgb() colors to hex, px dimensions to unitless
 * numbers.
 *
 * Files under a dimension folder (color-scheme/, density/) are resolved
 * in their own variant's context. Other files are resolved once per
 * color scheme (at the base density) and emitted per scheme only when
 * the resolved values actually differ.
 *
 * Usage: node scripts/generate-ts-tokens.mjs [--dtcg <dir>] [--css <dir>] [--out <dir>]
 */
import { mkdir, readFile, readdir, rm, writeFile } from 'node:fs/promises'
import { dirname, join, relative } from 'node:path'
import process from 'node:process'

const args = parseArgs(process.argv.slice(2))
const DTCG_DIR = args.dtcg ?? 'src/tokens/dtcg'
const CSS_DIR = args.css ?? 'src/tokens/css'
const OUT_DIR = args.out ?? 'src/tokens/ts'

// Must match the density base configured in the saved CSS/DTCG export
// configurations ("EDS-CSS" / "EDS-DTCG") in Tokens Studio
const BASE_DENSITY = 'comfortable'

const HEADER = `/**
 * Do not edit directly — generated by scripts/generate-ts-tokens.mjs
 * from the Tokens Studio DTCG + CSS exports.
 */
`

function parseArgs(argv) {
  const out = {}
  for (let i = 0; i < argv.length; i += 2) {
    const key = argv[i]?.replace(/^--/, '')
    const value = argv[i + 1]
    if (!key || !value) fail(`invalid arguments: ${argv.join(' ')}`)
    out[key] = value
  }
  return out
}

function fail(message) {
  console.error(`generate-ts-tokens: ${message}`)
  process.exit(1)
}

async function listFiles(dir, extension) {
  const entries = await readdir(dir, { recursive: true, withFileTypes: true })
  return (
    entries
      .filter((entry) => entry.isFile() && entry.name.endsWith(extension))
      // entry.path is the pre-Node-20.12 name for entry.parentPath
      .map((entry) =>
        relative(dir, join(entry.parentPath ?? entry.path, entry.name)),
      )
      .sort()
  )
}

/** Parse `--name: value;` declarations from a CSS export file. */
function parseCssVariables(css) {
  const variables = new Map()
  for (const match of css.matchAll(/--([\w-]+):\s*([^;]+);/g)) {
    variables.set(match[1], match[2].trim())
  }
  return variables
}

/** Merge the CSS variable maps that make up one dimension context. */
function buildContext(cssFiles, scheme, density) {
  const context = new Map()
  const parts = [
    'colors/default',
    'primitives/default',
    'font/default',
    'semantic/default',
    `color-scheme/${scheme}`,
    `density/${density}`,
  ]
  for (const part of parts) {
    const variables = cssFiles.get(`${part}.css`)
    if (!variables) fail(`missing CSS export file: ${part}.css`)
    for (const [name, value] of variables) context.set(name, value)
  }
  return context
}

/** Follow `var(--x)` chains until a concrete value is reached. */
function dereference(name, context) {
  const seen = new Set()
  let current = context.get(name)
  if (current === undefined) fail(`CSS variable not found: --${name}`)
  while (true) {
    const reference = /^var\(--([\w-]+)\)$/.exec(current)
    if (!reference) return current
    if (seen.has(reference[1]))
      fail(`circular var() chain at --${reference[1]}`)
    seen.add(reference[1])
    current = context.get(reference[1])
    if (current === undefined)
      fail(`broken var() chain: --${name} → --${reference[1]}`)
  }
}

/** OKLab → linear sRGB. */
function oklabToLinearSrgb(lightness, a, b) {
  const l = (lightness + 0.3963377774 * a + 0.2158037573 * b) ** 3
  const m = (lightness - 0.1055613458 * a - 0.0638541728 * b) ** 3
  const s = (lightness - 0.0894841775 * a - 1.291485548 * b) ** 3
  return [
    4.0767416621 * l - 3.3077115913 * m + 0.2309699292 * s,
    -1.2684380046 * l + 2.6097574011 * m - 0.3413193965 * s,
    -0.0041960863 * l - 0.7034186147 * m + 1.707614701 * s,
  ]
}

/** Linear sRGB → OKLab (inverse of the above, for deltaE). */
function linearSrgbToOklab([red, green, blue]) {
  const l = Math.cbrt(
    0.4122214708 * red + 0.5363325363 * green + 0.0514459929 * blue,
  )
  const m = Math.cbrt(
    0.2119034982 * red + 0.6806995451 * green + 0.1073969566 * blue,
  )
  const s = Math.cbrt(
    0.0883024619 * red + 0.2817188376 * green + 0.6299787005 * blue,
  )
  return [
    0.2104542553 * l + 0.793617785 * m - 0.0040720468 * s,
    1.9779984951 * l - 2.428592205 * m + 0.4505937099 * s,
    0.0259040371 * l + 0.7827717662 * m - 0.808675766 * s,
  ]
}

const inSrgbGamut = (linear) => linear.every((c) => c >= 0 && c <= 1)
const clip = (linear) => linear.map((c) => Math.min(1, Math.max(0, c)))

function linearToHex(linear) {
  const channels = clip(linear).map((channel) => {
    const gamma =
      channel <= 0.0031308
        ? 12.92 * channel
        : 1.055 * channel ** (1 / 2.4) - 0.055
    return Math.round(gamma * 255)
  })
  return `#${channels.map((c) => c.toString(16).padStart(2, '0')).join('')}`
}

/**
 * oklch(L C H) → #rrggbb. Out-of-gamut colors are gamut-mapped per
 * CSS Color 4 §13.2 (chroma bisection in oklch with deltaEOK ≤ 0.02
 * clip shortcut) so the result matches what browsers/lightningcss
 * produce for the CSS export — not naive channel clipping.
 */
function oklchToHex(value) {
  const match = /^oklch\(([\d.]+) ([\d.]+) ([\d.]+)\)$/.exec(value)
  if (!match) fail(`unsupported oklch form: ${value}`)
  const [lightness, chroma, hueDegrees] = match.slice(1).map(Number)
  if (lightness >= 1) return '#ffffff'
  if (lightness <= 0) return '#000000'
  const hue = (hueDegrees * Math.PI) / 180
  const toLinear = (c) =>
    oklabToLinearSrgb(lightness, c * Math.cos(hue), c * Math.sin(hue))

  const direct = toLinear(chroma)
  if (inSrgbGamut(direct)) return linearToHex(direct)

  const JND = 0.02
  const EPSILON = 0.0001
  let low = 0
  let high = chroma
  while (high - low > EPSILON) {
    const mid = (low + high) / 2
    const mapped = toLinear(mid)
    if (inSrgbGamut(mapped)) {
      low = mid
    } else {
      const clipped = linearSrgbToOklab(clip(mapped))
      const candidate = [lightness, mid * Math.cos(hue), mid * Math.sin(hue)]
      const deltaE = Math.hypot(
        clipped[0] - candidate[0],
        clipped[1] - candidate[1],
        clipped[2] - candidate[2],
      )
      if (deltaE < JND) return linearToHex(mapped)
      high = mid
    }
  }
  return linearToHex(toLinear(low))
}

/** rgb(r g b / a) → #rrggbbaa. */
function rgbToHex(value) {
  const match = /^rgb\((\d+) (\d+) (\d+) \/ ([\d.]+)\)$/.exec(value)
  if (!match) fail(`unsupported rgb form: ${value}`)
  const [red, green, blue] = match.slice(1, 4).map(Number)
  const alpha = Math.round(Number(match[4]) * 255)
  return `#${[red, green, blue, alpha].map((c) => c.toString(16).padStart(2, '0')).join('')}`
}

function convertValue(rawValue, type, cssName) {
  if (type === 'color') {
    if (rawValue.startsWith('#')) return rawValue
    if (rawValue.startsWith('oklch(')) return oklchToHex(rawValue)
    if (rawValue.startsWith('rgb(')) return rgbToHex(rawValue)
    fail(`unsupported color value for --${cssName}: ${rawValue}`)
  }
  if (
    ['dimension', 'number', 'fontWeight', 'fontSize', 'lineHeight'].includes(
      type,
    )
  ) {
    const numeric = /^(-?\d*\.?\d+)(px)?$/.exec(rawValue)
    if (!numeric) fail(`non-numeric ${type} for --${cssName}: ${rawValue}`)
    return Number(numeric[1])
  }
  if (type === 'fontFamily' || type === 'text' || type === 'string') {
    return rawValue
  }
  fail(`unknown $type "${type}" for --${cssName}`)
}

const DIGIT_WORDS = [
  'zero',
  'one',
  'two',
  'three',
  'four',
  'five',
  'six',
  'seven',
  'eight',
  'nine',
  'ten',
]

/** `corner-radius` → cornerRadius, `2xl` → twoXl; pure numbers stay as-is
 * (they get quoted during serialization in toTsSource). */
function tsKey(segment) {
  if (/^\d+$/.test(segment)) return segment
  const sized = /^(\d+)([a-z]+)$/.exec(segment)
  let normalized = segment
  if (sized) {
    const word = DIGIT_WORDS[Number(sized[1])]
    if (!word) fail(`unmappable numeric prefix: ${segment}`)
    normalized = `${word}-${sized[2]}`
  }
  return normalized.replace(/-([a-z0-9])/g, (_, letter) => letter.toUpperCase())
}

/** Mirror the CSS export's kebab casing: `readOnly` → `read-only`. */
function cssSegment(segment) {
  return segment.replace(/([a-z0-9])([A-Z])/g, '$1-$2').toLowerCase()
}

/** Recursively resolve a DTCG group into a plain value object. */
function resolveGroup(group, path, context) {
  if (group.$value !== undefined) {
    const cssName = `eds-${path.map(cssSegment).join('-')}`
    const rawValue = dereference(cssName, context)
    return convertValue(rawValue, group.$type, cssName)
  }
  const resolved = {}
  for (const [key, child] of Object.entries(group)) {
    if (key.startsWith('$')) continue
    resolved[tsKey(key)] = resolveGroup(child, [...path, key], context)
  }
  return resolved
}

const IDENTIFIER = /^[A-Za-z_$][\w$]*$/
const singleQuoted = (text) =>
  `'${text.replace(/\\/g, '\\\\').replace(/'/g, "\\'")}'`

/** Serialize a resolved value object as a Prettier-conformant TS literal
 * (single quotes with proper escaping, trailing commas, 2-space indent). */
function toLiteral(value, indent) {
  if (typeof value === 'number') return String(value)
  if (typeof value === 'string') return singleQuoted(value)
  const inner = `${indent}  `
  const entries = Object.entries(value).map(([key, child]) => {
    const tsName = IDENTIFIER.test(key) ? key : singleQuoted(key)
    return `${inner}${tsName}: ${toLiteral(child, inner)},`
  })
  if (entries.length === 0) return '{}'
  return `{\n${entries.join('\n')}\n${indent}}`
}

function toTsSource(exportName, value) {
  const typeName = exportName[0].toUpperCase() + exportName.slice(1)
  return `${HEADER}\nexport const ${exportName} = ${toLiteral(value, '')} as const\n\nexport type ${typeName} = typeof ${exportName}\n`
}

function deepEqual(a, b) {
  return JSON.stringify(a) === JSON.stringify(b)
}

const cssFiles = new Map()
for (const file of await listFiles(CSS_DIR, '.css')) {
  cssFiles.set(
    file,
    parseCssVariables(await readFile(join(CSS_DIR, file), 'utf8')),
  )
}
const dtcgFiles = await listFiles(DTCG_DIR, '.json')
if (dtcgFiles.length === 0) fail(`no DTCG files found in ${DTCG_DIR}`)

const schemes = dtcgFiles
  .filter((file) => file.startsWith('color-scheme/'))
  .map((file) => file.replace('color-scheme/', '').replace('.json', ''))
if (schemes.length === 0)
  fail('no color-scheme variants found in the DTCG export')

const contexts = new Map()
const contextFor = (scheme, density) => {
  const key = `${scheme}/${density}`
  if (!contexts.has(key))
    contexts.set(key, buildContext(cssFiles, scheme, density))
  return contexts.get(key)
}

await rm(OUT_DIR, { recursive: true, force: true })
const written = []

for (const file of dtcgFiles) {
  let tree = JSON.parse(await readFile(join(DTCG_DIR, file), 'utf8'))
  const [folder, variantFile] = file.split('/')
  const variant = variantFile.replace('.json', '')
  const exportName = tsKey(folder).replace(/'/g, '')

  // Hoist a single self-named wrapper group (e.g. density/*.json nests
  // everything under a "density" key) so modules don't read density.density.*
  let basePath = []
  const rootKeys = Object.keys(tree).filter((key) => !key.startsWith('$'))
  if (
    rootKeys.length === 1 &&
    tsKey(rootKeys[0]).replace(/'/g, '') === exportName
  ) {
    basePath = [rootKeys[0]]
    tree = tree[rootKeys[0]]
  }

  /** @type {Array<[outPath, resolvedValue]>} */
  let outputs
  if (folder === 'color-scheme') {
    outputs = [
      [file, resolveGroup(tree, basePath, contextFor(variant, BASE_DENSITY))],
    ]
  } else if (folder === 'density') {
    outputs = [
      // density tokens are dimension values and never reference
      // scheme-varying colours, so any scheme works as context
      [file, resolveGroup(tree, basePath, contextFor(schemes[0], variant))],
    ]
  } else {
    // Dimension-independent file: resolve once per scheme (at the base
    // density) and only split into per-scheme modules when values differ
    const perScheme = schemes.map((scheme) => [
      `${folder}/${scheme}.json`,
      resolveGroup(tree, basePath, contextFor(scheme, BASE_DENSITY)),
    ])
    outputs = perScheme.every(([, value]) => deepEqual(value, perScheme[0][1]))
      ? [[file, perScheme[0][1]]]
      : perScheme
  }

  for (const [outFile, value] of outputs) {
    const outPath = join(OUT_DIR, outFile.replace('.json', '.ts'))
    await mkdir(dirname(outPath), { recursive: true })
    await writeFile(outPath, toTsSource(exportName, value))
    written.push(relative(OUT_DIR, outPath))
  }
}

console.log(
  `generate-ts-tokens: wrote ${written.length} module(s) to ${OUT_DIR}`,
)
for (const file of written) console.log(`  ${file}`)
